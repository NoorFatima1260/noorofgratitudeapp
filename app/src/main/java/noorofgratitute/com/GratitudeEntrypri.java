package noorofgratitute.com;
public class GratitudeEntrypri {
    private int id;
    private String date;
    private String gratitudeText;
    public GratitudeEntrypri(int id, String date, String gratitudeText) {
        this.id = id;
        this.date = date;
        this.gratitudeText = gratitudeText;}
    public int getId() {
        return id;
    }
    public String getDate() {
        return date;
    }
    public String getGratitudeText() {
        return gratitudeText;
    }}
